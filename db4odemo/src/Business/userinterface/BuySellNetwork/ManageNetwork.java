    private void RegisterBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegisterBtnActionPerformed
        // TODO add your handling code here:

        if (txtRegister.getText().isEmpty())
            JOptionPane.showMessageDialog(null, "Please add network name");
        else {
            for (Netwrk nw : system.getNtwkLst()) //from ecosystem
            {
                if (txtRegister.getText().equalsIgnoreCase(nw.getNetwrkName())) {    //from Network we will check if network already exits or not if yes show message
                    JOptionPane.showMessageDialog(null, "Network Already Exists !");
                    return;
                }
            }
            String networkname = txtRegister.getText();
            Netwrk network = system.createAndAddNetwrk();  //from ecosystem
            network.setNetwrkName(networkname);
            populateTable();
            txtRegister.setText("");
        }
    }//GEN-LAST:event_RegisterBtnActionPerformed

