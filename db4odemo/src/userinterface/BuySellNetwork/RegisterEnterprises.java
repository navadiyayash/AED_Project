    private void SubmitBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitBtnActionPerformed
        // TODO add your handling code here:
        Netwrk network = (Netwrk) cbNetwork.getSelectedItem();
        Enterprise.EPType type = (Enterprise.EPType) cbEnterType.getSelectedItem();
        if (network == null || type == null || txtName.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Invalid Enterprise Name!");
            return;
        }
        String name = txtName.getText();
        System.out.print("Name log = " + name);
        for (Enterprise e : network.getEnpr().getEpLst()) {
            if (e.getName().equalsIgnoreCase(name)) {
                JOptionPane.showMessageDialog(null, "Enterprise exists with this name!");
                return;
            }
        }
        Enterprise enterprise = network.getEnpr().createAndAddEnterprise(name, type);
        populateTable();
        JOptionPane.showMessageDialog(null, "Enterprise successfully created!");
        txtName.setText("");
    }//GEN-LAST:event_SubmitBtnActionPerformed

