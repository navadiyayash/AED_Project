    private void populateTable() {
//        throw new UnsupportedOperationException("Not supported yet."); //To change body of generated methods, choose Tools | Templates.

        DefaultTableModel model = (DefaultTableModel) tblEnterprise.getModel();
        model.setRowCount(0);
        for (Netwrk network : system.getNtwkLst()) {
            for (Enterprise enterprise : network.getEnpr().getEpLst()) {
                Object[] row = new Object[3];
//                System.out.print("\n Name row : "+enterprise.getName());
                row[0] = enterprise.getName();
                row[1] = network.getNetwrkName();
                row[2] = enterprise.getEPType().getValue();
                model.addRow(row);
            }
        }
    }

    private void populateCB() {
//        throw new UnsupportedOperationException("Not supported yet."); //To change body of generated methods, choose Tools | Templates.
        cbNetwork.removeAllItems();
        cbEnterType.removeAllItems();

        for (Netwrk network : system.getNtwkLst()) {
            cbNetwork.addItem(network);
        }

        for (Enterprise.EPType type : Enterprise.EPType.values()) {
            cbEnterType.addItem(type);
        }

    }
