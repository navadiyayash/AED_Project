    private void populateRoleComboBox(Organizations organization) {
        cbRole.removeAllItems();
        for (Role role : organization.getSupportedRole()) {
            cbRole.addItem(role);
        }
    }

    public void populateMechanicComboBox(Organizations organization) {
        cbMechanic.removeAllItems();

        for (User user : organization.getUserDir().getUserLst()) {
            cbMechanic.addItem(user);
        }
    }

    public void populateData() {

        DefaultTableModel model = (DefaultTableModel) tblRole.getModel();

        model.setRowCount(0);

        for (Organizations organization : enpr.getOrgDir().getOrgLst()) {
            for (UserAcc uac : organization.getUserAccDir().getUserAccs()) {
                Object row[] = new Object[2];
                row[0] = uac;
                row[1] = uac.getRole();
                ((DefaultTableModel) tblRole.getModel()).addRow(row);
            }
        }
    }

