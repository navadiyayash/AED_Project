    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        Organizations organization = (Organizations) cbCompany.getSelectedItem();
        String PTRN = "^[a-zA-Z0-9_]{0,30}[@][a-zA-Z0-9]{0,10}[.][a-zA-Z]{0,10}$";

        String U_Name = txtUserName.getText();
        Role role = (Role) cbRole.getSelectedItem();

        String pass = txtPassword.getText();
        User user = (User) cbMechanic.getSelectedItem();

        Pattern ptrn = Pattern.compile(PTRN);
        Matcher match = ptrn.matcher(txtUserName.getText());

        if (txtUserName.getText().isEmpty() || txtPassword.getText().isEmpty() || txtRePass.getText().isEmpty() || cbSecurityAns.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "You will need to enter required fields!");
            return;
        } else if (!txtPassword.getText().equals(txtRePass.getText())) {
            JOptionPane.showMessageDialog(null, "Password doesn't match");
            return;
        } else if (!match.matches()) {
            JOptionPane.showMessageDialog(null, "You will need to enter correct Email address formate");
            return;
        } else {
            if (organization.getUserAccDir().checkIfUsernameIsUnique(U_Name)) {
                UserAcc uac = organization.getUserAccDir().createUserAcc(U_Name, pass, user, role);
                uac.setSecQ((String) cbSecurityQue.getSelectedItem());
                uac.setSecA(cbSecurityAns.getText());

                uac.setVal(1);
                populateData();

                JOptionPane.showMessageDialog(null, "Mechanic has been registered succefully!");

                txtUserName.setText("");
                txtPassword.setText("");
            } else {
                JOptionPane.showMessageDialog(null, "User name is in database / please enter right information");
                return;
            }
        }

    }//GEN-LAST:event_btnCreateActionPerformed

