    public void populateCompany() {
        cbCompany.removeAllItems();

        for (Organizations organization : enpr.getOrgDir().getOrgLst()) {
            cbCompany.addItem(organization);
        }
    }
//populate mechanic combo box
    public void populateDoctorComboBox(Organizations organization) {
        cbMechanic.removeAllItems();

        for (User user : organization.getUserDir().getUserLst()) {
            cbMechanic.addItem(user);
        }
    }
//populate role combobox
    private void populateRoleComboBox(Organizations organization) {
        cbRole.removeAllItems();
        for (Role role : organization.getSupportedRole()) {
            cbRole.addItem(role);
        }
    }

    public void popData() {

        DefaultTableModel model = (DefaultTableModel) tblMechanic.getModel();

        model.setRowCount(0);

        for (Organizations organization : enpr.getOrgDir().getOrgLst()) {
            for (UserAcc ua : organization.getUserAccDir().getUserAccs()) {
                Object row[] = new Object[2];
                row[0] = ua;
                row[1] = ua.getRole();
                ((DefaultTableModel) tblMechanic.getModel()).addRow(row);
            }
        }
    }

