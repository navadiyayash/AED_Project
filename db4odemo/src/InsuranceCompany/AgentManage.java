    private void txtUserNameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtUserNameActionPerformed
        // TODO add your handling code here:
        
//        show is the user is entering correct username or not
        String PTRN = "^[a-zA-Z0-9_]{0,30}[@][a-zA-Z0-9]{0,10}[.][a-zA-Z]{0,10}$";
        Pattern patt = Pattern.compile(PTRN);
        Matcher match = patt.matcher(txtUserName.getText());
        if (!match.matches()) {
            lblUser.setText("Incorrect Username");
        } else {
            lblUser.setText(null);
        }
    }//GEN-LAST:event_txtUserNameActionPerformed

    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:

        String userName = txtUserName.getText();
        String password = txtPassword.getText();
        Organizations organization = (Organizations) cbCompany.getSelectedItem();
        User user = (User) cbMechanic.getSelectedItem();
        Role role = (Role) cbRole.getSelectedItem();

        String PATTERN = "^[a-zA-Z0-9_]{0,30}[@][a-zA-Z0-9]{0,10}[.][a-zA-Z]{0,10}$";
        Pattern patt = Pattern.compile(PATTERN);
        Matcher match = patt.matcher(txtUserName.getText());

        if (txtUserName.getText().isEmpty() || txtPassword.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please enter username & password!");
            return;
        } else if (!match.matches()) {
            JOptionPane.showMessageDialog(null, "Please enter proper emailid");
            return;
        } else {
            if (organization.getUserAccDir().checkIfUsernameIsUnique(userName)) {
                UserAcc ua = organization.getUserAccDir().createUserAcc(userName, password, user, role);
                ua.setSecQ((String) cbSecQue.getSelectedItem());
                ua.setSecA(txtSecurityAns.getText());

                popData();

                JOptionPane.showMessageDialog(null, "Agent Added Successfully!");
//blank text
                txtUserName.setText("");
                txtPassword.setText("");

            } else {
                JOptionPane.showMessageDialog(null, "UserName Already Exists!");
            }

        }
    }//GEN-LAST:event_btnCreateActionPerformed

