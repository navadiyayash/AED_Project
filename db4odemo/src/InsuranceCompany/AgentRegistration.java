    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        Organizations organization = (Organizations) cbOrg.getSelectedItem();
        String name = txtAgentName.getText();

        if (txtAgentName.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please enter Loan Agents name!");
            return;
        } else {
            for (User user : organization.getUserDir().getUserLst()) {
                if (txtAgentName.getText().equalsIgnoreCase(user.getU_name())) {
                    JOptionPane.showMessageDialog(null, "This Agent already exists !");
                    return;
                }
            }
            organization.getUserDir().createUser(name);
            populateTable(organization);

            JOptionPane.showMessageDialog(null, "Agent added successfully!");

            txtAgentName.setText("");
        }
    }//GEN-LAST:event_btnAddActionPerformed


