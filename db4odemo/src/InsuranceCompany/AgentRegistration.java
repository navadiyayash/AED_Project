    public void populateOrgComboBox() {
        cbOrg.removeAllItems();

        for (Organizations organization : orgdir.getOrgLst()) {
            cbOrg.addItem(organization);
        }
    }

    private void populateTable(Organizations organization) {
        DefaultTableModel model = (DefaultTableModel) tblAgent.getModel();

        model.setRowCount(0);

        for (User user : organization.getUserDir().getUserLst()) {
            Object[] row = new Object[2];
            row[0] = user.getU_id();
            row[1] = user.getU_name();
            model.addRow(row);
        }
    }
