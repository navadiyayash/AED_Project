    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:

        Organizations organization = (Organizations) cbOrgEmp.getSelectedItem();
        String uuname = txtName.getText();

        if (txtName.getText().isEmpty()) {

            JOptionPane.showMessageDialog(null, "Please enter user's name!");
            return;
        } else if (cbOrgEmp.getItemCount() == 0 || cbOrgEmp.getItemCount() == 0) {
            JOptionPane.showMessageDialog(null, "You need to select the organization");
            return;
        } else {
            for (User ur : organization.getUserDir().getUserLst()) {
                if (txtName.getText().equalsIgnoreCase(ur.getU_name())) {
                    JOptionPane.showMessageDialog(null, "User name is already in database!");
                    return;
                }
            }
            organization.getUserDir().createUser(uuname);
            populateTable(organization);

            JOptionPane.showMessageDialog(null, "User created without any issue!");
            txtName.setText("");
        }
    }//GEN-LAST:event_btnCreateActionPerformed

